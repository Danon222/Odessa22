"""Написати функцію square, що приймає 1 аргумент - сторону квадрата, і повертає 3 значення (за допомогою кортежу): периметр квадрата, площа квадрата та діагональ квадрата.
"""

def square(a):
    p = a * 4
    s = a * a
    d = (2 * s) ** (1 / 2)

    return [p, s, round(d, 2)]

a = float(input('Введите сторону квадрата: '))
print(square(a))